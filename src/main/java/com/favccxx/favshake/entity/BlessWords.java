package com.favccxx.favshake.entity;

import java.util.Date;

public class BlessWords {
	
	private String blessId;
	
	private int blessNum;
	
	private String blessType;
	
	private String blessCategory;
	
	private String blessContent;
	
	private String blessURL;
	
	private Date createDate;
	
	private String createUserName;
	
	private String createUserEmail;

	public String getBlessId() {
		return blessId;
	}

	public void setBlessId(String blessId) {
		this.blessId = blessId;
	}

	public String getBlessType() {
		return blessType;
	}

	public void setBlessType(String blessType) {
		this.blessType = blessType;
	}

	public String getBlessCategory() {
		return blessCategory;
	}

	public void setBlessCategory(String blessCategory) {
		this.blessCategory = blessCategory;
	}

	public String getBlessContent() {
		return blessContent;
	}

	public void setBlessContent(String blessContent) {
		this.blessContent = blessContent;
	}

	public String getBlessURL() {
		return blessURL;
	}

	public void setBlessURL(String blessURL) {
		this.blessURL = blessURL;
	}

	public Date getCreateDate() {
		return createDate;
	}

	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}

	public int getBlessNum() {
		return blessNum;
	}

	public void setBlessNum(int blessNum) {
		this.blessNum = blessNum;
	}

	public String getCreateUserName() {
		return createUserName;
	}

	public void setCreateUserName(String createUserName) {
		this.createUserName = createUserName;
	}

	public String getCreateUserEmail() {
		return createUserEmail;
	}

	public void setCreateUserEmail(String createUserEmail) {
		this.createUserEmail = createUserEmail;
	}
	
	

}
