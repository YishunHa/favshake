package com.favccxx.favshake.entity;

import java.util.Date;

public class Sponsor {
	
	private String sponsorId;
	
	private String sponsorName;
	
	private String sponsorEmail;
	
	private Date sponsorDate;
	
	private String sponsorType;
	
	private String sponsorCash;

	
	public String getSponsorId() {
		return sponsorId;
	}

	public void setSponsorId(String sponsorId) {
		this.sponsorId = sponsorId;
	}

	public String getSponsorName() {
		return sponsorName;
	}

	public void setSponsorName(String sponsorName) {
		this.sponsorName = sponsorName;
	}

	public String getSponsorEmail() {
		return sponsorEmail;
	}

	public void setSponsorEmail(String sponsorEmail) {
		this.sponsorEmail = sponsorEmail;
	}

	public Date getSponsorDate() {
		return sponsorDate;
	}

	public void setSponsorDate(Date sponsorDate) {
		this.sponsorDate = sponsorDate;
	}

	public String getSponsorType() {
		return sponsorType;
	}

	public void setSponsorType(String sponsorType) {
		this.sponsorType = sponsorType;
	}

	public String getSponsorCash() {
		return sponsorCash;
	}

	public void setSponsorCash(String sponsorCash) {
		this.sponsorCash = sponsorCash;
	}
	
	
	

}
